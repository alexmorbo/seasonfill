func softDeleteSeriesCacheSQL(driver string) string {
	switch driver {
	case "postgres":
		return `UPDATE series_cache SET deleted_at = now() WHERE instance_name = $1 AND sonarr_series_id = $2`
	case "sqlite":
		return `UPDATE series_cache SET deleted_at = CURRENT_TIMESTAMP WHERE instance_name = ? AND sonarr_series_id = ?`
	}
	panic("unknown driver " + driver)
}

func countActiveSeriesCache(t *testing.T, ctx context.Context, db *sql.DB, driver, instance string) int {
	t.Helper()
	var q string
	switch driver {
	case "postgres":
		q = `SELECT COUNT(*) FROM series_cache WHERE instance_name = $1 AND deleted_at IS NULL`
	case "sqlite":
		q = `SELECT COUNT(*) FROM series_cache WHERE instance_name = ? AND deleted_at IS NULL`
	default:
		t.Fatalf("unknown driver %q", driver)
	}
	var cnt int
	row := db.QueryRowContext(ctx, q, instance)
	require.NoError(t, row.Scan(&cnt))
	return cnt
}
