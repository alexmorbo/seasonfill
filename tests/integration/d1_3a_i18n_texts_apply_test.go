//go:build integration

// D-1-3a (story 456a) — verifies 000001 + 000002 apply cleanly on both
// backends, exercises insert + FK + composite PK enforcement on the
// new i18n tables, and rolls back to clean state. Uses the shared
// d1_helpers extracted from D-1-2 (see d1_helpers_test.go).
package integration

import (
	"context"
	"testing"
	"time"

	"github.com/google/uuid"
	"github.com/stretchr/testify/require"
)

func TestD13a_I18nTextsMigrationRoundTrip(t *testing.T) {
	for _, b := range allD1Backends(t) {
		t.Run(b.name, func(t *testing.T) {
			ctx, cancel := context.WithTimeout(context.Background(), 60*time.Second)
			defer cancel()

			db, m, cleanup := b.migrate(t)
			t.Cleanup(cleanup)

			// UP — applies 000001 (core series) then 000002 (i18n texts).
			require.NoError(t, m.Up())

			// Seed: insert a series row to satisfy FK target.
			seriesTitle := "d1-3a-" + uuid.NewString()
			_, err := db.ExecContext(ctx, insertSeriesSQL(b.name),
				seriesTitle, "stub", false, "[]")
			require.NoError(t, err)

			// Resolve the autogenerated id portably across drivers
			// (PG / pgx doesn't expose LastInsertId; SQLite does, but
			// the SELECT-by-title path works for both).
			var seriesID int64
			row := db.QueryRowContext(ctx, selectSeriesIDByTitleSQL(b.name), seriesTitle)
			require.NoError(t, row.Scan(&seriesID))
			require.Greater(t, seriesID, int64(0))

			// Happy path: insert series_texts row.
			_, err = db.ExecContext(ctx,
				insertSeriesTextSQL(b.name),
				seriesID, "en-US", "English Title", "English overview", nil)
			require.NoError(t, err, "series_texts insert should succeed")

			// Composite PK enforcement: re-insert same (series_id, language) must fail.
			_, err = db.ExecContext(ctx,
				insertSeriesTextSQL(b.name),
				seriesID, "en-US", "DUPE", "DUPE", nil)
			require.Error(t, err, "duplicate (series_id, language) should violate PK")

			// Different language is fine.
			_, err = db.ExecContext(ctx,
				insertSeriesTextSQL(b.name),
				seriesID, "ru-RU", "Русский заголовок", "Описание", nil)
			require.NoError(t, err, "second language should succeed")

			// FK enforcement: orphan series_texts insert must fail.
			_, err = db.ExecContext(ctx,
				insertSeriesTextSQL(b.name),
				999999, "en-US", "Orphan", "Orphan", nil)
			require.Error(t, err, "orphan series_texts (no parent series) should fail FK")

			// episode_texts: orphan insert must fail.
			_, err = db.ExecContext(ctx,
				insertEpisodeTextSQL(b.name),
				999999, "en-US", "Orphan", "Orphan")
			require.Error(t, err, "orphan episode_texts should fail FK")

			// DOWN — rolls back 000002 (i18n texts go away) then 000001.
			require.NoError(t, m.Down())
			_, err = db.ExecContext(ctx, "SELECT 1 FROM series_texts LIMIT 1")
			require.Error(t, err, "series_texts should be dropped after Down")
			_, err = db.ExecContext(ctx, "SELECT 1 FROM episode_texts LIMIT 1")
			require.Error(t, err, "episode_texts should be dropped after Down")
		})
	}
}

func selectSeriesIDByTitleSQL(driver string) string {
	switch driver {
	case "postgres":
		return `SELECT id FROM series WHERE title = $1`
	case "sqlite":
		return `SELECT id FROM series WHERE title = ?`
	}
	panic("unknown driver " + driver)
}

func insertSeriesTextSQL(driver string) string {
	switch driver {
	case "postgres":
		return `INSERT INTO series_texts (series_id, language, title, overview, tagline, updated_at)
		        VALUES ($1, $2, $3, $4, $5, now())`
	case "sqlite":
		return `INSERT INTO series_texts (series_id, language, title, overview, tagline, updated_at)
		        VALUES (?, ?, ?, ?, ?, CURRENT_TIMESTAMP)`
	}
	panic("unknown driver " + driver)
}

func insertEpisodeTextSQL(driver string) string {
	switch driver {
	case "postgres":
		return `INSERT INTO episode_texts (episode_id, language, title, overview, updated_at)
		        VALUES ($1, $2, $3, $4, now())`
	case "sqlite":
		return `INSERT INTO episode_texts (episode_id, language, title, overview, updated_at)
		        VALUES (?, ?, ?, ?, CURRENT_TIMESTAMP)`
	}
	panic("unknown driver " + driver)
}
