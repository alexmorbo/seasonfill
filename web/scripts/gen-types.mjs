// Generates web/src/api/schema.ts from ../docs/swagger.yaml.
import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs/promises";
import openapiTS, { astToString } from "openapi-typescript";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const specPath = path.resolve(root, "..", "docs", "swagger.yaml");
const outPath = path.resolve(root, "src", "api", "schema.ts");

const ast = await openapiTS(new URL(`file://${specPath}`),
  { exportType: true, immutable: true, enum: true });
const header = "/* eslint-disable */\n// Generated by gen-types.mjs. DO NOT EDIT — run `make openapi`.\n\n";
await fs.mkdir(path.dirname(outPath), { recursive: true });
await fs.writeFile(outPath, header + astToString(ast), "utf8");
console.log(`wrote ${outPath}`);
