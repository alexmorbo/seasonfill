import { useQuery } from '@tanstack/react-query';
import { ApiError, api } from './api';

export type Session = { username: string; autoGenerated: boolean };

const SESSION_KEY = ['auth', 'session'] as const;

// Wire-shape from GET /api/v1/auth/session. snake_case → camelCase
// happens in this module so callers see the JS shape only.
type SessionResponse = { ok: true; username: string; auto_generated: boolean };

export async function getSession(): Promise<Session> {
  const r = await api<SessionResponse>('/auth/session');
  return { username: r.username, autoGenerated: r.auto_generated };
}

export function useSession() {
  return useQuery({
    queryKey: SESSION_KEY,
    queryFn: getSession,
    retry: (n, err) => {
      if (err instanceof ApiError && err.status === 401) return false;
      return n < 2;
    },
    staleTime: 30_000,
    refetchOnWindowFocus: false,
  });
}

export const sessionQueryKey = SESSION_KEY;

export async function loginWithPassword(input: {
  username: string;
  password: string;
}): Promise<void> {
  // Backend returns 200 + {ok, username, auto_generated} envelope;
  // we discard the body — the cookie is what matters and the
  // subsequent useSession() refetch surfaces username + autoGenerated
  // to the UI via the canonical path.
  await api<unknown>('/auth/login', { method: 'POST', body: input });
}

export async function logout(): Promise<void> {
  await api<void>('/auth/session', { method: 'DELETE' });
}

// `new` is a reserved word in JS; param renamed locally. Wire field
// stays `new` to match the backend DTO (021a-1a §17 PasswordChangeRequest).
// Consumed by the password-change modal in 021b-2.
export async function changePassword(input: {
  current: string;
  newPassword: string;
}): Promise<void> {
  await api<void>('/auth/password', {
    method: 'POST',
    body: { current: input.current, new: input.newPassword },
  });
}
