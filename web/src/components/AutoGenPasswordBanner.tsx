import { useState } from 'react';
import { useTranslation } from 'react-i18next';
import { AlertTriangle, X } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { useSession } from '@/lib/auth';
import { useAuthConfig } from '@/lib/auth-config';
import { PasswordChangeDialog } from './PasswordChangeDialog';

const DISMISS_KEY = 'seasonfill.autogen_banner.dismissed';

function readDismissed(): boolean {
  try {
    return sessionStorage.getItem(DISMISS_KEY) === '1';
  } catch {
    // sessionStorage may be unavailable (private mode / SSR) — silent fallback
    return false;
  }
}

export function AutoGenPasswordBanner() {
  const { t } = useTranslation();
  const { data, isSuccess } = useSession();
  const cfg = useAuthConfig();
  const [dismissed, setDismissed] = useState<boolean>(readDismissed);
  const [dialogOpen, setDialogOpen] = useState(false);

  // Banner is meaningless outside Forms mode: under Basic / None we don't
  // own the credential dialogue. Until auth-config resolves, treat it
  // optimistically as forms so we don't briefly flash + then hide the
  // banner on first paint.
  const mode = cfg.data?.mode ?? 'forms';
  if (mode !== 'forms') return null;
  if (!isSuccess || !data?.autoGenerated || dismissed) return null;

  const dismiss = () => {
    try { sessionStorage.setItem(DISMISS_KEY, '1'); } catch { /* ignore */ }
    setDismissed(true);
  };

  return (
    <>
      <div
        role="alert"
        className="flex items-center gap-3 px-4 py-2 bg-status-warning/10 border-b border-status-warning/40 text-status-warning text-[12.5px]"
      >
        <AlertTriangle className="w-4 h-4 shrink-0" aria-hidden="true" />
        <span className="flex-1">
          {t('autoGenBanner.message')}
        </span>
        <Button
          type="button"
          variant="outline"
          size="sm"
          onClick={() => setDialogOpen(true)}
        >
          {t('nav.changePassword')}
        </Button>
        <button
          type="button"
          aria-label={t('autoGenBanner.dismiss')}
          onClick={dismiss}
          className="grid place-items-center w-6 h-6 rounded hover:bg-status-warning/20"
        >
          <X className="w-3.5 h-3.5" />
        </button>
      </div>
      <PasswordChangeDialog open={dialogOpen} onOpenChange={setDialogOpen} />
    </>
  );
}
