import { describe, expect, it, vi, afterEach, beforeEach } from 'vitest';
import userEvent from '@testing-library/user-event';
import { screen } from '@testing-library/react';
import { renderWithProviders } from '@/test-utils';
import { AutoGenPasswordBanner } from './AutoGenPasswordBanner';
import * as auth from '@/lib/auth';
import * as authConfig from '@/lib/auth-config';

afterEach(() => { vi.restoreAllMocks(); sessionStorage.clear(); });
beforeEach(() => { sessionStorage.clear(); });

function mockSession(data: { username: string; autoGenerated: boolean } | null) {
  vi.spyOn(auth, 'useSession').mockReturnValue({
    data: data ?? undefined,
    isSuccess: data !== null,
    isPending: false,
    isError: false,
    error: null,
  } as unknown as ReturnType<typeof auth.useSession>);
}

function mockCfg(mode: 'forms' | 'basic' | 'none') {
  vi.spyOn(authConfig, 'useAuthConfig').mockReturnValue({
    data: { mode },
    isSuccess: true, isPending: false, isError: false, error: null,
  } as unknown as ReturnType<typeof authConfig.useAuthConfig>);
}

describe('<AutoGenPasswordBanner />', () => {
  it('renders when session.autoGenerated=true under mode=forms', () => {
    mockCfg('forms');
    mockSession({ username: 'admin', autoGenerated: true });
    renderWithProviders(<AutoGenPasswordBanner />);
    expect(screen.getByRole('alert')).toHaveTextContent(/auto-generated password/i);
    expect(screen.getByRole('button', { name: /change password/i })).toBeInTheDocument();
  });

  it('renders nothing when session.autoGenerated=false', () => {
    mockCfg('forms');
    mockSession({ username: 'admin', autoGenerated: false });
    renderWithProviders(<AutoGenPasswordBanner />);
    expect(screen.queryByRole('alert')).not.toBeInTheDocument();
  });

  it('renders nothing while session is unresolved', () => {
    mockCfg('forms');
    mockSession(null);
    renderWithProviders(<AutoGenPasswordBanner />);
    expect(screen.queryByRole('alert')).not.toBeInTheDocument();
  });

  it('renders nothing under mode=basic even with autoGenerated=true', () => {
    mockCfg('basic');
    mockSession({ username: 'admin', autoGenerated: true });
    renderWithProviders(<AutoGenPasswordBanner />);
    expect(screen.queryByRole('alert')).not.toBeInTheDocument();
  });

  it('renders nothing under mode=none even with autoGenerated=true', () => {
    mockCfg('none');
    mockSession({ username: 'admin', autoGenerated: true });
    renderWithProviders(<AutoGenPasswordBanner />);
    expect(screen.queryByRole('alert')).not.toBeInTheDocument();
  });

  it('hides after dismiss + persists in sessionStorage', async () => {
    mockCfg('forms');
    mockSession({ username: 'admin', autoGenerated: true });
    const { unmount } = renderWithProviders(<AutoGenPasswordBanner />);
    await userEvent.click(screen.getByRole('button', { name: /dismiss banner/i }));
    expect(screen.queryByRole('alert')).not.toBeInTheDocument();
    unmount();
    renderWithProviders(<AutoGenPasswordBanner />);
    expect(screen.queryByRole('alert')).not.toBeInTheDocument();
  });
});
