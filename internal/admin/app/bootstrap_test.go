type fakeAdminRepo struct {
	mu      sync.Mutex
	user    *admin.User
	getErr  error
	created []admin.User
}

func (r *fakeAdminRepo) Get(_ context.Context) (admin.User, error) {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.getErr != nil {
		return admin.User{}, r.getErr
	}
	if r.user == nil {
		return admin.User{}, ports.ErrNotFound
	}
	return *r.user, nil
}
func (r *fakeAdminRepo) Create(_ context.Context, u admin.User) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	r.created = append(r.created, u)
	r.user = &u
	return nil
}
func (r *fakeAdminRepo) UpdatePassword(_ context.Context, _ uint, hash string) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.user == nil {
		return ports.ErrNotFound
	}
	r.user.PasswordHash = hash
	r.user.AutoGenerated = false
	return nil
}
func (r *fakeAdminRepo) UpdateLastLoginAt(_ context.Context, _ uint, when time.Time) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.user == nil {
		return ports.ErrNotFound
	}
	t := when
	r.user.LastLoginAt = &t
	return nil
}
func (r *fakeAdminRepo) GetByOIDCSubject(_ context.Context, _ string) (admin.User, error) {
	return admin.User{}, ports.ErrNotFound
}
func (r *fakeAdminRepo) CreateFromOIDC(_ context.Context, subject, username, email string) (admin.User, error) {
	sub := subject
	u := admin.User{Username: username, OIDCSubject: &sub}
	if email != "" {
		e := email
		u.Email = &e
	}
	r.mu.Lock()
	r.created = append(r.created, u)
	r.user = &u
	r.mu.Unlock()
	return u, nil
}

func TestBootstrap_ExistingRow_NoOp(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{user: &admin.User{Username: "admin", PasswordHash: "old"}}
	autoGen, err := Bootstrap(t.Context(), repo, BootstrapConfig{WebUser: "admin"}, slog.Default())
	require.NoError(t, err)
	assert.False(t, autoGen, "existing row → autoGen must be false")
	assert.Empty(t, repo.created)
}

func TestBootstrap_PreHashed(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{}
	pre := "$2a$12$preexistinghash"
	autoGen, err := Bootstrap(t.Context(), repo,
		BootstrapConfig{WebUser: "admin", WebPasswordHash: pre}, slog.Default())
	require.NoError(t, err)
	assert.False(t, autoGen, "pre-hashed path → autoGen must be false")
	require.Len(t, repo.created, 1)
	assert.Equal(t, pre, repo.created[0].PasswordHash)
	assert.False(t, repo.created[0].AutoGenerated)
}

func TestBootstrap_PlainPassword(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{}
	autoGen, err := Bootstrap(t.Context(), repo,
		BootstrapConfig{WebUser: "admin", WebPassword: "hunter22"}, slog.Default())
	require.NoError(t, err)
	assert.False(t, autoGen, "explicit password → autoGen must be false")
	require.Len(t, repo.created, 1)
	assert.False(t, repo.created[0].AutoGenerated)
	assert.NoError(t, bcrypt.CompareHashAndPassword(
		[]byte(repo.created[0].PasswordHash), []byte("hunter22")))
}

func TestBootstrap_AutoGenerated(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{}
	autoGen, err := Bootstrap(t.Context(), repo,
		BootstrapConfig{WebUser: "admin"}, slog.Default())
	require.NoError(t, err)
	assert.True(t, autoGen, "no creds supplied → autoGen must be true")
	require.Len(t, repo.created, 1)
	assert.True(t, repo.created[0].AutoGenerated)
	assert.NotEmpty(t, repo.created[0].PasswordHash)
}

func TestBootstrap_RepoError(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{getErr: errors.New("db down")}
	_, err := Bootstrap(t.Context(), repo, BootstrapConfig{}, slog.Default())
	require.Error(t, err)
	assert.Contains(t, err.Error(), "db down")
}

// TestBootstrap_PopulatesEmail_WhenEnvSet covers the 466a new wiring:
// SEASONFILL_WEB_EMAIL flows through BootstrapConfig.WebEmail and lands
// on the seeded user's Email pointer.
func TestBootstrap_PopulatesEmail_WhenEnvSet(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{}
	_, err := Bootstrap(t.Context(), repo,
		BootstrapConfig{WebUser: "admin", WebPassword: "hunter22", WebEmail: "admin@morbo.dev"},
		slog.Default())
	require.NoError(t, err)
	require.Len(t, repo.created, 1)
	require.NotNil(t, repo.created[0].Email)
	assert.Equal(t, "admin@morbo.dev", *repo.created[0].Email)
}

