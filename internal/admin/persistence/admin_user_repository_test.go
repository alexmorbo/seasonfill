func TestAdminUserRepo_GetByOIDCSubject_Found(t *testing.T) {
	t.Parallel()
	for _, backend := range testhelpers.AllBackends(t) {
		t.Run(backend.Name, func(t *testing.T) {
			t.Parallel()
			repo := NewAdminUserRepository(backend.NewDB(t))
			got, err := repo.CreateFromOIDC(t.Context(), "sub|abc123", "alice")
			require.NoError(t, err)
			require.NotZero(t, got.ID)

			found, err := repo.GetByOIDCSubject(t.Context(), "sub|abc123")
			require.NoError(t, err)
			assert.Equal(t, "alice", found.Username)
			assert.Equal(t, "sub|abc123", *found.OIDCSubject)
			assert.Empty(t, found.PasswordHash, "OIDC user must have no password hash")
			assert.False(t, found.AutoGenerated)
		})
	}
}

