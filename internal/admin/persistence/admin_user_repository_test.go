func TestAdminUserRepo_CreateFromOIDC_MultipleUsers(t *testing.T) {
	t.Parallel()
	for _, backend := range testhelpers.AllBackends(t) {
		t.Run(backend.Name, func(t *testing.T) {
			t.Parallel()
			repo := NewAdminUserRepository(backend.NewDB(t))
			u1, err := repo.CreateFromOIDC(t.Context(), "sub|1", "user1")
			require.NoError(t, err)
			u2, err := repo.CreateFromOIDC(t.Context(), "sub|2", "user2")
			require.NoError(t, err)

			assert.NotEqual(t, u1.ID, u2.ID)
			assert.Equal(t, "sub|1", *u1.OIDCSubject)
			assert.Equal(t, "sub|2", *u2.OIDCSubject)

			// GetByOIDCSubject resolves each one independently.
			r1, err := repo.GetByOIDCSubject(t.Context(), "sub|1")
			require.NoError(t, err)
			assert.Equal(t, "user1", r1.Username)

			r2, err := repo.GetByOIDCSubject(t.Context(), "sub|2")
			require.NoError(t, err)
			assert.Equal(t, "user2", r2.Username)
		})
	}
}
