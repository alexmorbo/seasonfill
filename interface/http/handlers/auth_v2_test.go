type fakeAdminRepoV2 struct {
	mu   sync.Mutex
	user *admin.AdminUser
	err  error
}

func (r *fakeAdminRepoV2) Get(_ context.Context) (admin.AdminUser, error) {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.err != nil {
		return admin.AdminUser{}, r.err
	}
	if r.user == nil {
		return admin.AdminUser{}, ports.ErrNotFound
	}
	return *r.user, nil
}
func (r *fakeAdminRepoV2) Create(_ context.Context, u admin.AdminUser) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	r.user = &u
	return nil
}
func (r *fakeAdminRepoV2) UpdatePassword(_ context.Context, hash string, ag bool) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.user == nil {
		return ports.ErrNotFound
	}
	r.user.PasswordHash = hash
	r.user.AutoGenerated = ag
	return nil
}

var v2HandlerOnce sync.Once

func setupV2Handler(t *testing.T, repo ports.AdminUserRepository, lim *auth.IPLimiter) (*gin.Engine, *AuthHandlerV2) {
	t.Helper()
	v2HandlerOnce.Do(func() { gin.SetMode(gin.TestMode) })
	r := gin.New()
	h := NewAuthHandlerV2("apikey-xyz", repo, time.Hour, false, lim, slog.Default())
	r.POST("/api/v1/auth/login", h.Login)
	g := r.Group("")
	g.Use(middleware.RequireAuthV2("apikey-xyz"))
	g.DELETE("/api/v1/auth/session", h.Logout)
	g.GET("/api/v1/auth/session", h.Session)
	g.POST("/api/v1/auth/password", h.PasswordChange)
	return r, h
}

func seedV2(t *testing.T) *fakeAdminRepoV2 {
	t.Helper()
	hash, err := auth.HashPassword("hunter22")
	require.NoError(t, err)
	return &fakeAdminRepoV2{user: &admin.AdminUser{
		Username: "admin", PasswordHash: hash, AutoGenerated: false,
	}}
}

func postJSONV2(t *testing.T, r *gin.Engine, path string, body any, c *http.Cookie) *httptest.ResponseRecorder {
	t.Helper()
	raw, _ := json.Marshal(body)
	req := httptest.NewRequestWithContext(t.Context(), http.MethodPost, path, bytes.NewReader(raw))
	req.Header.Set("Content-Type", "application/json")
	if c != nil {
		req.AddCookie(c)
	}
	w := httptest.NewRecorder()
	r.ServeHTTP(w, req)
	return w
}

func TestV2Login_Success(t *testing.T) {
	t.Parallel()
	r, _ := setupV2Handler(t, seedV2(t), nil)
	w := postJSONV2(t, r, "/api/v1/auth/login",
		map[string]string{"username": "admin", "password": "hunter22"}, nil)
	require.Equal(t, http.StatusOK, w.Code, "body=%s", w.Body.String())
	var found *http.Cookie
	for _, c := range w.Result().Cookies() {
		if c.Name == middleware.NewSessionCookieName {
			found = c
		}
	}
	require.NotNil(t, found)
	assert.True(t, found.HttpOnly)
	assert.Equal(t, http.SameSiteStrictMode, found.SameSite)
}

