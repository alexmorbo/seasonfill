func TestV2Session_ReturnsAutoGenerated(t *testing.T) {
	t.Parallel()
	repo := seedV2(t)
	repo.user.AutoGenerated = true
	r, h := setupV2Handler(t, repo, nil)
	tok, _ := middleware.SignSession([]byte(h.apiKey), "admin", time.Now().Add(time.Hour))
	req := httptest.NewRequestWithContext(t.Context(), http.MethodGet,
		"/api/v1/auth/session", nil)
	req.AddCookie(&http.Cookie{Name: middleware.NewSessionCookieName, Value: tok})
	w := httptest.NewRecorder()
	r.ServeHTTP(w, req)
	require.Equal(t, http.StatusOK, w.Code)
	var resp map[string]any
	require.NoError(t, json.Unmarshal(w.Body.Bytes(), &resp))
	assert.Equal(t, true, resp["auto_generated"])
	assert.Equal(t, "admin", resp["username"])
}
