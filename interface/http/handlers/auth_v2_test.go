func TestV2Login_RateLimit(t *testing.T) {
	t.Parallel()
	lim := auth.NewIPLimiter(rate.Every(time.Hour), 2)
	r, _ := setupV2Handler(t, seedV2(t), lim)
	for i := 0; i < 2; i++ {
		w := postJSONV2(t, r, "/api/v1/auth/login",
			map[string]string{"username": "admin", "password": "wrong"}, nil)
		assert.Equal(t, http.StatusUnauthorized, w.Code, "attempt %d", i+1)
	}
	w := postJSONV2(t, r, "/api/v1/auth/login",
		map[string]string{"username": "admin", "password": "hunter22"}, nil)
	assert.Equal(t, http.StatusTooManyRequests, w.Code)
}

