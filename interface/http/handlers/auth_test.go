func TestAuthPasswordChange_Success(t *testing.T) {
	t.Parallel()
	repo := seedRepo(t)
	r, h := setupAuth(t, repo, nil)
	tok, _ := middleware.SignSession([]byte(h.apiKey), "admin", time.Now().Add(time.Hour))
	w := postJSON(t, r, "/api/v1/auth/password",
		map[string]string{"current": "hunter22", "new": "newpassword123"},
		&http.Cookie{Name: middleware.SessionCookieName, Value: tok})
	require.Equal(t, http.StatusNoContent, w.Code, "body=%s", w.Body.String())
	assert.True(t, auth.VerifyPassword(repo.user.PasswordHash, "newpassword123"))
	assert.False(t, repo.user.AutoGenerated)
}

