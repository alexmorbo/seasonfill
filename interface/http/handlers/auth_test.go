// TestAuthPasswordChange_RateLimit covers M1: after burst exhaustion
// the endpoint MUST return 429 without touching bcrypt or the repo.
func TestAuthPasswordChange_RateLimit(t *testing.T) {
	t.Parallel()
	repo := seedRepo(t)
	// Burst 2, refill once per hour — guaranteed exhaustion mid-test.
	pwLim := auth.NewIPLimiter(rate.Every(time.Hour), 2)
	r, h := setupAuthWithOpts(t, repo, nil, WithPasswordLimiter(pwLim))
	tok, _ := middleware.SignSession(h.sessionKey, "admin", time.Now().Add(time.Hour), 0)
	cookie := &http.Cookie{Name: middleware.SessionCookieName, Value: tok}
	// Two wrong-current attempts consume the burst.
	for i := 0; i < 2; i++ {
		w := postJSON(t, r, "/api/v1/auth/password",
			map[string]string{"current": "wrong", "new": "newpassword123"}, cookie)
		assert.Equal(t, http.StatusUnauthorized, w.Code, "attempt %d", i+1)
	}
	// Third — even with correct credentials — is throttled.
	w := postJSON(t, r, "/api/v1/auth/password",
		map[string]string{"current": "hunter22", "new": "newpassword123"}, cookie)
	require.Equal(t, http.StatusTooManyRequests, w.Code, "body=%s", w.Body.String())
	assert.Contains(t, w.Body.String(), "RATE_LIMITED")
}

// TestLogin_SecureCookieAtomicLive — flipping AuthRuntime.SecureCookie via
// the shared atomic (the reload subscriber's only side effect) must take
// effect on the very next Login without rebuilding the handler.
func TestLogin_SecureCookieAtomicLive(t *testing.T) {
	t.Parallel()
	repo := seedRepo(t)
	r, h := setupAuth(t, repo, nil)
	// Boot atomic = SecureCookie:false (setupAuth passes false).
	w := postJSON(t, r, "/api/v1/auth/login",
		map[string]string{"username": "admin", "password": "hunter22"}, nil)
	require.Equal(t, http.StatusOK, w.Code, "body=%s", w.Body.String())
	var cookie *http.Cookie
	for _, c := range w.Result().Cookies() {
		if c.Name == middleware.SessionCookieName {
			cookie = c
		}
	}
	require.NotNil(t, cookie)
	assert.False(t, cookie.Secure, "boot=false → Secure must be false on plain HTTP")

	// Flip via the atomic (simulates AuthMiddlewareSubscriber.apply).
	h.authRuntime.Store(&middleware.AuthRuntime{
		SessionTTL:   time.Hour,
		SecureCookie: true,
	})
	w = postJSON(t, r, "/api/v1/auth/login",
		map[string]string{"username": "admin", "password": "hunter22"}, nil)
	require.Equal(t, http.StatusOK, w.Code)
	cookie = nil
	for _, c := range w.Result().Cookies() {
		if c.Name == middleware.SessionCookieName {
			cookie = c
		}
	}
	require.NotNil(t, cookie)
	assert.True(t, cookie.Secure, "after atomic flip → next Login must set Secure")
}

// E-4: Logout

// TestLogout_ClearsCookie asserts that DELETE /api/v1/auth/session responds
// 204 and sets the session cookie to Max-Age=-1 (clearing it).
func TestLogout_ClearsCookie(t *testing.T) {
	t.Parallel()
	repo := seedRepo(t)
	r, h := setupAuth(t, repo, nil)

	// First obtain a valid session cookie via Login.
	w := postJSON(t, r, "/api/v1/auth/login",
		map[string]string{"username": "admin", "password": "hunter22"}, nil)
	require.Equal(t, http.StatusOK, w.Code, "login must succeed: %s", w.Body.String())
	var sessionCookie *http.Cookie
	for _, c := range w.Result().Cookies() {
		if c.Name == middleware.SessionCookieName {
			sessionCookie = c
		}
	}
	require.NotNil(t, sessionCookie, "login response must set session cookie")

	// Build a signed token via the handler's session key (same key the
	// middleware uses for validation) and call Logout with it.
	tok, err := middleware.SignSession(h.sessionKey, "admin", time.Now().Add(time.Hour), 0)
	require.NoError(t, err)
	req := httptest.NewRequestWithContext(t.Context(), http.MethodDelete,
		"/api/v1/auth/session", nil)
	req.AddCookie(&http.Cookie{Name: middleware.SessionCookieName, Value: tok})
	wLogout := httptest.NewRecorder()
	r.ServeHTTP(wLogout, req)
	require.Equal(t, http.StatusNoContent, wLogout.Code, "body=%s", wLogout.Body.String())

	// The response must contain a Set-Cookie header that clears the cookie
	// (MaxAge < 0, indicated by the raw header containing "max-age=-1").
	var clearedCookie *http.Cookie
	for _, c := range wLogout.Result().Cookies() {
		if c.Name == middleware.SessionCookieName {
			clearedCookie = c
		}
	}
	require.NotNil(t, clearedCookie, "Logout must emit Set-Cookie for session cookie")
	assert.True(t, clearedCookie.MaxAge < 0,
		"Logout must set Max-Age < 0 to clear the cookie, got %d", clearedCookie.MaxAge)
}

