type fakeAdminRepo struct {
	mu   sync.Mutex
	user *admin.AdminUser
	err  error
}

func (r *fakeAdminRepo) Get(_ context.Context) (admin.AdminUser, error) {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.err != nil {
		return admin.AdminUser{}, r.err
	}
	if r.user == nil {
		return admin.AdminUser{}, ports.ErrNotFound
	}
	return *r.user, nil
}
func (r *fakeAdminRepo) Create(_ context.Context, u admin.AdminUser) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	r.user = &u
	return nil
}
func (r *fakeAdminRepo) UpdatePassword(_ context.Context, hash string, ag bool) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.user == nil {
		return ports.ErrNotFound
	}
	r.user.PasswordHash = hash
	r.user.AutoGenerated = ag
	return nil
}

func setupAuth(t *testing.T, repo ports.AdminUserRepository, lim *auth.IPLimiter) (*gin.Engine, *AuthHandler) {
	return setupAuthWithOpts(t, repo, lim)
}

func setupAuthWithOpts(t *testing.T, repo ports.AdminUserRepository, lim *auth.IPLimiter, opts ...AuthOption) (*gin.Engine, *AuthHandler) {
	t.Helper()
	const testAPIKey = "apikey-xyz"
	sessionKey, err := crypto.DeriveSessionHMACKey(testAPIKey)
	require.NoError(t, err)
	r := gin.New()
	h := NewAuthHandler(testAPIKey, repo, time.Hour, false, lim, slog.Default(), opts...)
	r.POST("/api/v1/auth/login", h.Login)
	g := r.Group("")
	g.Use(middleware.RequireAuth(testAPIKey, sessionKey))
	g.DELETE("/api/v1/auth/session", h.Logout)
	g.GET("/api/v1/auth/session", h.Session)
	g.POST("/api/v1/auth/password", h.PasswordChange)
	return r, h
}

func seedRepo(t *testing.T) *fakeAdminRepo {
	t.Helper()
	hash, err := auth.HashPassword("hunter22")
	require.NoError(t, err)
	return &fakeAdminRepo{user: &admin.AdminUser{
		Username: "admin", PasswordHash: hash, AutoGenerated: false,
	}}
}

func postJSON(t *testing.T, r *gin.Engine, path string, body any, c *http.Cookie) *httptest.ResponseRecorder {
	t.Helper()
	raw, _ := json.Marshal(body)
	req := httptest.NewRequestWithContext(t.Context(), http.MethodPost, path, bytes.NewReader(raw))
	req.Header.Set("Content-Type", "application/json")
	if c != nil {
		req.AddCookie(c)
	}
	w := httptest.NewRecorder()
	r.ServeHTTP(w, req)
	return w
}

