package auth

import (
	"context"
	"errors"
	"log/slog"
	"sync"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
	"golang.org/x/crypto/bcrypt"

	"github.com/alexmorbo/seasonfill/application/ports"
	"github.com/alexmorbo/seasonfill/domain/admin"
)

type fakeAdminRepo struct {
	mu      sync.Mutex
	user    *admin.AdminUser
	getErr  error
	created []admin.AdminUser
}

func (r *fakeAdminRepo) Get(_ context.Context) (admin.AdminUser, error) {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.getErr != nil {
		return admin.AdminUser{}, r.getErr
	}
	if r.user == nil {
		return admin.AdminUser{}, ports.ErrNotFound
	}
	return *r.user, nil
}
func (r *fakeAdminRepo) Create(_ context.Context, u admin.AdminUser) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	r.created = append(r.created, u)
	r.user = &u
	return nil
}
func (r *fakeAdminRepo) UpdatePassword(_ context.Context, hash string, ag bool) error {
	r.mu.Lock()
	defer r.mu.Unlock()
	if r.user == nil {
		return ports.ErrNotFound
	}
	r.user.PasswordHash = hash
	r.user.AutoGenerated = ag
	return nil
}
func (r *fakeAdminRepo) GetByOIDCSubject(_ context.Context, _ string) (admin.AdminUser, error) {
	return admin.AdminUser{}, ports.ErrNotFound
}
func (r *fakeAdminRepo) CreateFromOIDC(_ context.Context, subject, username string) (admin.AdminUser, error) {
	sub := subject
	u := admin.AdminUser{Username: username, OIDCSubject: &sub}
	r.mu.Lock()
	r.created = append(r.created, u)
	r.user = &u
	r.mu.Unlock()
	return u, nil
}

func TestBootstrap_ExistingRow_NoOp(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{user: &admin.AdminUser{Username: "admin", PasswordHash: "old"}}
	require.NoError(t, Bootstrap(t.Context(), repo, BootstrapConfig{WebUser: "admin"}, slog.Default()))
	assert.Empty(t, repo.created)
}

func TestBootstrap_PreHashed(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{}
	pre := "$2a$12$preexistinghash"
	require.NoError(t, Bootstrap(t.Context(), repo,
		BootstrapConfig{WebUser: "admin", WebPasswordHash: pre}, slog.Default()))
	require.Len(t, repo.created, 1)
	assert.Equal(t, pre, repo.created[0].PasswordHash)
	assert.False(t, repo.created[0].AutoGenerated)
}

func TestBootstrap_PlainPassword(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{}
	require.NoError(t, Bootstrap(t.Context(), repo,
		BootstrapConfig{WebUser: "admin", WebPassword: "hunter22"}, slog.Default()))
	require.Len(t, repo.created, 1)
	assert.False(t, repo.created[0].AutoGenerated)
	assert.NoError(t, bcrypt.CompareHashAndPassword(
		[]byte(repo.created[0].PasswordHash), []byte("hunter22")))
}

func TestBootstrap_AutoGenerated(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{}
	require.NoError(t, Bootstrap(t.Context(), repo,
		BootstrapConfig{WebUser: "admin"}, slog.Default()))
	require.Len(t, repo.created, 1)
	assert.True(t, repo.created[0].AutoGenerated)
	assert.NotEmpty(t, repo.created[0].PasswordHash)
}

func TestBootstrap_RepoError(t *testing.T) {
	t.Parallel()
	repo := &fakeAdminRepo{getErr: errors.New("db down")}
	err := Bootstrap(t.Context(), repo, BootstrapConfig{}, slog.Default())
	require.Error(t, err)
	assert.Contains(t, err.Error(), "db down")
}

func TestGeneratePassword_Length(t *testing.T) {
	t.Parallel()
	pw, err := GeneratePassword(24)
	require.NoError(t, err)
	assert.Len(t, pw, 24)
}

func TestConstantLatencyVerify_UnknownUserFalse(t *testing.T) {
	t.Parallel()
	assert.False(t, ConstantLatencyVerify("", "anything"))
}
