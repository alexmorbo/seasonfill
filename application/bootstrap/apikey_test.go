package bootstrap

import (
	"context"
	"log/slog"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"

	"github.com/alexmorbo/seasonfill/application/ports"
	"github.com/alexmorbo/seasonfill/internal/runtime"
	"github.com/alexmorbo/seasonfill/internal/runtime/crypto"
)

type mockRuntimeConfigRepository struct {
	row    ports.RuntimeConfigRow
	hasRow bool
	err    error
}

func (m *mockRuntimeConfigRepository) Get(ctx context.Context) (ports.RuntimeConfigRow, error) {
	if !m.hasRow {
		return ports.RuntimeConfigRow{}, ports.ErrNotFound
	}
	return m.row, m.err
}

func (m *mockRuntimeConfigRepository) Upsert(ctx context.Context, snap runtime.Snapshot) error {
	return nil
}

func (m *mockRuntimeConfigRepository) SaveAPIKey(ctx context.Context, ct []byte, autoGen bool) error {
	m.row.APIKeyCiphertext = ct
	m.row.APIKeyAutoGenerated = autoGen
	m.hasRow = true
	return nil
}

func TestResolveAPIKey_ProvidedKeyValidatesProbe(t *testing.T) {
	ctx := context.Background()
	log := slog.Default()

	key := "my-master-key"
	cipher, err := crypto.New(key)
	require.NoError(t, err)

	probe, err := cipher.Seal([]byte(key))
	require.NoError(t, err)

	repo := &mockRuntimeConfigRepository{
		hasRow: true,
		row:    ports.RuntimeConfigRow{APIKeyCiphertext: probe},
	}

	resolved, err := ResolveAPIKey(ctx, key, repo, log)
	require.NoError(t, err)
	assert.Equal(t, key, resolved)
}

func TestResolveAPIKey_ProvidedKeyMismatch(t *testing.T) {
	ctx := context.Background()
	log := slog.Default()

	correctKey := "correct-key"
	cipher, err := crypto.New(correctKey)
	require.NoError(t, err)

	probe, err := cipher.Seal([]byte(correctKey))
	require.NoError(t, err)

	repo := &mockRuntimeConfigRepository{
		hasRow: true,
		row:    ports.RuntimeConfigRow{APIKeyCiphertext: probe},
	}

	_, err = ResolveAPIKey(ctx, "wrong-key", repo, log)
	require.Error(t, err)
	assert.ErrorIs(t, err, ErrAPIKeyMismatch)
}

func TestResolveAPIKey_NoKeyNoRowAutoGen(t *testing.T) {
	ctx := context.Background()
	log := slog.Default()

	repo := &mockRuntimeConfigRepository{hasRow: false}

	resolved, err := ResolveAPIKey(ctx, "", repo, log)
	require.NoError(t, err)
	assert.NotEmpty(t, resolved)
	assert.True(t, repo.hasRow)
	assert.True(t, repo.row.APIKeyAutoGenerated)
	assert.NotEmpty(t, repo.row.APIKeyCiphertext)
}

func TestResolveAPIKey_NoKeyWithRowFails(t *testing.T) {
	ctx := context.Background()
	log := slog.Default()

	cipher, err := crypto.New("existing-key")
	require.NoError(t, err)
	probe, err := cipher.Seal([]byte("existing-key"))
	require.NoError(t, err)

	repo := &mockRuntimeConfigRepository{
		hasRow: true,
		row:    ports.RuntimeConfigRow{APIKeyCiphertext: probe},
	}

	_, err = ResolveAPIKey(ctx, "", repo, log)
	require.ErrorIs(t, err, ErrAPIKeyMismatch)
}

func TestResolveAPIKey_ProvidedKeySavesProbe(t *testing.T) {
	ctx := context.Background()
	log := slog.Default()

	repo := &mockRuntimeConfigRepository{hasRow: false}

	key := "new-key"
	resolved, err := ResolveAPIKey(ctx, key, repo, log)
	require.NoError(t, err)
	assert.Equal(t, key, resolved)
	assert.True(t, repo.hasRow)
	assert.False(t, repo.row.APIKeyAutoGenerated)
	assert.NotEmpty(t, repo.row.APIKeyCiphertext)

	// Verify the saved probe can be opened with the same key.
	cipher, err := crypto.New(key)
	require.NoError(t, err)
	plaintext, err := cipher.Open(repo.row.APIKeyCiphertext)
	require.NoError(t, err)
	assert.Equal(t, key, string(plaintext))
}
