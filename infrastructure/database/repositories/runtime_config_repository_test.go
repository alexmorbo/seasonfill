func TestRuntimeConfigRepository_UpsertAndGet(t *testing.T) {
	db := setupTestDB(t)
	repo := NewRuntimeConfigRepository(db, nil)
	ctx := context.Background()

	snap := runtime.Snapshot{
		Cron: runtime.CronSnapshot{
			Enabled:  true,
			Schedule: "0 12 * * *",
			OnStart:  false,
			Jitter:   2 * time.Minute,
		},
		Scan: runtime.ScanSnapshot{
			ShutdownGrace: 90 * time.Second,
			CooldownSweep: 20 * time.Minute,
		},
		DryRun: false,
		GlobalRateLimit: runtime.RateLimitSnapshot{
			RPM: 60, Burst: 20,
		},
		Auth: runtime.AuthSnapshot{
			SessionTTL:     24 * time.Hour,
			SecureCookie:   true,
			TrustedProxies: []string{"10.0.0.1", "10.0.0.2"},
		},
	}

	require.NoError(t, repo.Upsert(ctx, snap, nil))

	row, err := repo.Get(ctx)
	require.NoError(t, err)
	assert.Equal(t, snap.Cron.Enabled, row.Cron.Enabled)
	assert.Equal(t, snap.Cron.Schedule, row.Cron.Schedule)
	assert.Equal(t, snap.Cron.Jitter, row.Cron.Jitter)
	assert.Equal(t, snap.DryRun, row.DryRun)
	assert.Equal(t, snap.GlobalRateLimit.RPM, row.GlobalRateLimit.RPM)
	assert.Equal(t, snap.Auth.SessionTTL, row.Auth.SessionTTL)
	assert.Equal(t, snap.Auth.SecureCookie, row.Auth.SecureCookie)
	assert.Equal(t, snap.Auth.TrustedProxies, row.Auth.TrustedProxies)
}

