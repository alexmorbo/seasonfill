func TestRuntimeConfigRepository_Upsert_StaleIUS_Rejects(t *testing.T) {
	db := setupTestDB(t)
	repo := NewRuntimeConfigRepository(db, nil)
	ctx := context.Background()

	snap := runtime.Defaults()
	require.NoError(t, repo.Upsert(ctx, snap, nil))
	row, err := repo.Get(ctx)
	require.NoError(t, err)
	staleHeader := row.UpdatedAt.Add(-1 * time.Second)

	snap.Cron.Schedule = "0 0 * * *"
	require.ErrorIs(t, repo.Upsert(ctx, snap, &staleHeader), ports.ErrStaleWrite)

	got, err := repo.Get(ctx)
	require.NoError(t, err)
	assert.NotEqual(t, "0 0 * * *", got.Cron.Schedule, "stale write must not persist")
}

