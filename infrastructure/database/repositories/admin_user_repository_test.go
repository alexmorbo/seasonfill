package repositories

import (
	"errors"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"

	"github.com/alexmorbo/seasonfill/application/ports"
	"github.com/alexmorbo/seasonfill/domain/admin"
)

func TestAdminUserRepo_GetEmpty(t *testing.T) {
	t.Parallel()
	repo := NewAdminUserRepository(setupTestDB(t))
	_, err := repo.Get(t.Context())
	require.True(t, errors.Is(err, ports.ErrNotFound))
}

func TestAdminUserRepo_CreateThenGet(t *testing.T) {
	t.Parallel()
	repo := NewAdminUserRepository(setupTestDB(t))
	want := admin.AdminUser{Username: "admin", PasswordHash: "$2a$10$abc", AutoGenerated: true}
	require.NoError(t, repo.Create(t.Context(), want))
	got, err := repo.Get(t.Context())
	require.NoError(t, err)
	assert.Equal(t, want.Username, got.Username)
	assert.Equal(t, want.PasswordHash, got.PasswordHash)
	assert.True(t, got.AutoGenerated)
	assert.NotZero(t, got.CreatedAt)
}

func TestAdminUserRepo_UpdatePassword(t *testing.T) {
	t.Parallel()
	repo := NewAdminUserRepository(setupTestDB(t))
	require.NoError(t, repo.Create(t.Context(), admin.AdminUser{
		Username: "admin", PasswordHash: "old", AutoGenerated: true,
	}))
	require.NoError(t, repo.UpdatePassword(t.Context(), "new", false))
	got, err := repo.Get(t.Context())
	require.NoError(t, err)
	assert.Equal(t, "new", got.PasswordHash)
	assert.False(t, got.AutoGenerated)
}

func TestAdminUserRepo_UpdatePassword_NoRow(t *testing.T) {
	t.Parallel()
	repo := NewAdminUserRepository(setupTestDB(t))
	err := repo.UpdatePassword(t.Context(), "hash", false)
	require.True(t, errors.Is(err, ports.ErrNotFound))
}
