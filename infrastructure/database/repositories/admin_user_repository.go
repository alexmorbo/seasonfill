package repositories

import (
	"context"
	"errors"
	"fmt"
	"time"

	"gorm.io/gorm"

	"github.com/alexmorbo/seasonfill/application/ports"
	"github.com/alexmorbo/seasonfill/domain/admin"
	"github.com/alexmorbo/seasonfill/infrastructure/database"
)

type AdminUserRepository struct{ db *gorm.DB }

func NewAdminUserRepository(db *gorm.DB) *AdminUserRepository {
	return &AdminUserRepository{db: db}
}

func (r *AdminUserRepository) Get(ctx context.Context) (admin.AdminUser, error) {
	var m database.AdminUserModel
	err := dbFromContext(ctx, r.db).WithContext(ctx).First(&m).Error
	if err != nil {
		if errors.Is(err, gorm.ErrRecordNotFound) {
			return admin.AdminUser{}, ports.ErrNotFound
		}
		return admin.AdminUser{}, fmt.Errorf("get admin user: %w", err)
	}
	return admin.AdminUser{
		ID: m.ID, Username: m.Username, PasswordHash: m.PasswordHash,
		AutoGenerated: m.AutoGenerated, OIDCSubject: m.OIDCSubject,
		CreatedAt: m.CreatedAt, UpdatedAt: m.UpdatedAt,
	}, nil
}

func (r *AdminUserRepository) Create(ctx context.Context, u admin.AdminUser) error {
	now := time.Now().UTC()
	if u.CreatedAt.IsZero() {
		u.CreatedAt = now
	}
	if u.UpdatedAt.IsZero() {
		u.UpdatedAt = now
	}
	m := database.AdminUserModel{
		Username: u.Username, PasswordHash: u.PasswordHash,
		AutoGenerated: u.AutoGenerated, OIDCSubject: u.OIDCSubject,
		CreatedAt: u.CreatedAt, UpdatedAt: u.UpdatedAt,
	}
	if err := dbFromContext(ctx, r.db).WithContext(ctx).Create(&m).Error; err != nil {
		return fmt.Errorf("create admin user: %w", err)
	}
	return nil
}

// UpdatePassword updates the (single-user) admin row by primary key.
//
// LOW review-fix: the previous `Where("1=1")` worked under the
// single-row invariant but was brittle if a future migration ever
// inserted a second row. Read-then-update by ID is explicit and
// keeps the same NotFound contract: empty table → ErrNotFound.
func (r *AdminUserRepository) UpdatePassword(ctx context.Context, hash string, autoGenerated bool) error {
	db := dbFromContext(ctx, r.db).WithContext(ctx)
	var existing database.AdminUserModel
	if err := db.First(&existing).Error; err != nil {
		if errors.Is(err, gorm.ErrRecordNotFound) {
			return ports.ErrNotFound
		}
		return fmt.Errorf("update admin password: load row: %w", err)
	}
	res := db.Model(&database.AdminUserModel{}).
		Where("id = ?", existing.ID).
		Updates(map[string]any{
			"password_hash":  hash,
			"auto_generated": autoGenerated,
			"updated_at":     time.Now().UTC(),
		})
	if res.Error != nil {
		return fmt.Errorf("update admin password: %w", res.Error)
	}
	if res.RowsAffected == 0 {
		return ports.ErrNotFound
	}
	return nil
}

// GetByOIDCSubject looks up the admin row by OIDC subject claim.
// Returns ports.ErrNotFound when no row matches — the OIDC callback
// handler then falls through to CreateFromOIDC.
func (r *AdminUserRepository) GetByOIDCSubject(ctx context.Context, subject string) (admin.AdminUser, error) {
	var m database.AdminUserModel
	err := dbFromContext(ctx, r.db).WithContext(ctx).
		Where("oidc_subject = ?", subject).First(&m).Error
	if err != nil {
		if errors.Is(err, gorm.ErrRecordNotFound) {
			return admin.AdminUser{}, ports.ErrNotFound
		}
		return admin.AdminUser{}, fmt.Errorf("get admin by oidc subject: %w", err)
	}
	return admin.AdminUser{
		ID: m.ID, Username: m.Username, PasswordHash: m.PasswordHash,
		AutoGenerated: m.AutoGenerated, OIDCSubject: m.OIDCSubject,
		CreatedAt: m.CreatedAt, UpdatedAt: m.UpdatedAt,
	}, nil
}

// CreateFromOIDC inserts a new admin_users row keyed on the OIDC subject
// claim. Password hash is empty (OIDC users never authenticate via the
// password path). AutoGenerated=false reflects "no password to rotate".
// Called by the OIDC callback handler on first successful login.
func (r *AdminUserRepository) CreateFromOIDC(ctx context.Context, subject, username string) (admin.AdminUser, error) {
	now := time.Now().UTC()
	sub := subject
	m := database.AdminUserModel{
		Username:      username,
		PasswordHash:  "",
		AutoGenerated: false,
		OIDCSubject:   &sub,
		CreatedAt:     now,
		UpdatedAt:     now,
	}
	if err := dbFromContext(ctx, r.db).WithContext(ctx).Create(&m).Error; err != nil {
		return admin.AdminUser{}, fmt.Errorf("create oidc admin: %w", err)
	}
	return admin.AdminUser{
		ID: m.ID, Username: m.Username, PasswordHash: m.PasswordHash,
		AutoGenerated: m.AutoGenerated, OIDCSubject: m.OIDCSubject,
		CreatedAt: m.CreatedAt, UpdatedAt: m.UpdatedAt,
	}, nil
}

var _ ports.AdminUserRepository = (*AdminUserRepository)(nil)
